package sol_OOPs;

public class NotificationData {
	  private String to;
	    private String message;

	    public NotificationData(String to, String message) {
	        this.to = to;
	        this.message = message;
	    }

	    public String getTo() {
	        return to;
	    }

	    public String getMessage() {
	        return message;
	    }
}
